import Foundation // swift module


// let => constant
// var => variabel

let firstName = "John Doe"
print(firstName)

// bisa aja menambahkan tipe data di depan variabel
let secondName: String = "Jane Doe"

// Reference object yg sebelumnya dibuat
let username = secondName

// Boolean / Bool (True/False)
let isOn = true
print(isOn)

let isUserActive: Bool = false


// Swift is type-safe language


// Date
let hariIni: Date = Date()


// Number can be Int, Double, CGFloat & More

// Int is a whole Integer
let bilanganPertama: Int = 15

// Double for MATH
let bilanganKedua: Double = 1.5

// Use CGFloat for UI
let bilanganKetiga: CGFloat = 2.5
